package com.avi.java.LogicalLoop;



public class WhileLoop {
	public static void main(String[] args) 
	
	{
		int i=100;
		
		System.out.println("Odd nos from 100 to 1 : ");
		while(i>=1)
		{	
			System.out.print(i+", ");
		i=i-2;
		}
		
	    System.out.println("Even Values from 100 to 1 : ");
	   
	    
	    int j=99;
	   
	    while(j>=0)
	    {
	    	System.out.print(j+", ");
	    	j=j-2;
	    }
	}

}
